def to_word(number):
    converted = ''
    x=str(extraeDecimales(number))
    number_str = str(int(number)).zfill(9)
    millones = number_str[:3]
    miles = number_str[3:6]
    cientos = number_str[6:]

    if(millones):
        if(millones == '001'):
            converted += 'UN MILLON '
        elif(int(millones) > 0):
            converted += '%sMILLONES ' % __convert_group(millones)

    if(miles):
        if(miles == '001'):
            converted += 'MIL '
        elif(int(miles) > 0):
            converted += '%sMIL ' % __convert_group(miles)

    if(cientos):
        if(cientos == '001'):
            converted += 'UNO '
        elif(int(cientos) > 0):
            converted += '%s ' % __convert_group(cientos)

    return converted + 'CON ' + str(int(x)).zfill(2) + '/100'

def __convert_group(n):
    """Turn each group of numbers into letters"""
    output = ''

    if(n == '100'):
        output = "CIEN "
    elif(n[0] != '0'):
        output = CENTENAS[int(n[0]) - 1]

    k = int(n[1:])
    if(k <= 20):
        output += UNIDADES[k]
    else:
        if((k > 30) & (n[2] != '0')):
            output += '%sY %s' % (DECENAS[int(n[1]) - 2], UNIDADES[int(n[2])])
        else:
            output += '%s%s' % (DECENAS[int(n[1]) - 2], UNIDADES[int(n[2])])

    return output

def extraeDecimales(nNumero, max_digits=2):
        strDecimales = str( round(nNumero%1, 2) ).replace('0.','')
        strDecimales += "0"*max_digits
        strDecimales = strDecimales[00:max_digits]
        return long( strDecimales )
